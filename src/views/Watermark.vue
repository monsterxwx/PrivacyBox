// ---------------- æ–¹æ³• ----------------
const triggerUpload = () => fileInput.value.click()

const handleFileChange = (e) => {
  const file = e.target.files[0]
  if (!file) return

  const reader = new FileReader()
  reader.onload = (event) => {
    imageUrl.value = event.target.result
    loadImage(event.target.result)
  }
  reader.readAsDataURL(file)
}

const loadImage = (src) => {
  const img = new Image()
  img.onload = () => {
    originalImage = img
    draw()
  }
  img.src = src
}

// æ ¸å¿ƒç»˜åˆ¶é€»è¾‘
const draw = () => {
  if (!originalImage || !canvasRef.value) return

  const canvas = canvasRef.value
  const ctx = canvas.getContext('2d')

  // 1. è®¾ç½®ç”»å¸ƒå°ºå¯¸ä¸ºå›¾ç‰‡åŸå§‹å°ºå¯¸ (ä¿è¯é«˜æ¸…)
  canvas.width = originalImage.width
  canvas.height = originalImage.height

  // 2. ç»˜åˆ¶åŸå›¾
  ctx.drawImage(originalImage, 0, 0)

  // 3. é…ç½®æ°´å°æ ·å¼
  ctx.font = `bold ${config.fontSize * (canvas.width / 1000 * 2.5)}px sans-serif` // å­—ä½“å¤§å°éšå›¾ç‰‡å°ºå¯¸è‡ªé€‚åº”
  // å¦‚æœå›¾ç‰‡å¾ˆå°ï¼Œå­—ä½“ä¸èƒ½å¤ªå°ï¼ŒåŠ ä¸ªåŸºå‡†å€æ•°
  const baseScale = Math.max(1, canvas.width / 1000)
  ctx.font = `bold ${config.fontSize * baseScale}px sans-serif`

  ctx.fillStyle = config.color
  ctx.globalAlpha = config.opacity
  ctx.textBaseline = 'middle'
  ctx.textAlign = 'center'

  // 4. è®¡ç®—å¹³é“ºé€»è¾‘
  // ä¸ºäº†æ—‹è½¬åèƒ½è¦†ç›–å…¨å›¾ï¼Œç”Ÿæˆçš„ç½‘æ ¼åŒºåŸŸéœ€è¦æ¯”ç”»å¸ƒå¤§
  const text = config.text || 'Watermark'

  // åˆ†åˆ«è®¡ç®— X å’Œ Y çš„å®é™…åƒç´ é—´è·
  const gapX = config.gapX * baseScale
  const gapY = config.gapY * baseScale

  const rotateRad = config.rotate * Math.PI / 180

  // ä¿å­˜çŠ¶æ€è¿›è¡Œæ—‹è½¬
  ctx.translate(canvas.width / 2, canvas.height / 2)
  ctx.rotate(rotateRad)
  ctx.translate(-canvas.width / 2, -canvas.height / 2)

  // è®¡ç®—è¦†ç›–å¯¹è§’çº¿é•¿åº¦ï¼Œç¡®ä¿æ—‹è½¬åä¸ç•™ç™½
  const diagonal = Math.sqrt(canvas.width ** 2 + canvas.height ** 2)
  const limit = diagonal // å‘å››å‘¨æ‰©å±•èŒƒå›´

  // ä½¿ç”¨è¡Œç´¢å¼• (rowIndex) æ¥æ§åˆ¶é”™ä½ï¼Œæ¯”ç”¨åæ ‡è®¡ç®—æ›´å‡†ç¡®
  let rowIndex = 0

  // å¤–å±‚å¾ªç¯æ§åˆ¶ Y è½´ (å‚ç›´/è¡Œ)
  for (let y = -limit; y < limit * 2; y += gapY) {
    // é”™ä½æ’åˆ—é€»è¾‘ï¼šå¶æ•°è¡Œä¸åç§»ï¼Œå¥‡æ•°è¡Œå‘å³åç§»ä¸€åŠçš„ gapX
    const offsetX = rowIndex % 2 === 0 ? 0 : gapX / 2

    // å†…å±‚å¾ªç¯æ§åˆ¶ X è½´ (æ°´å¹³)
    for (let x = -limit; x < limit * 2; x += gapX) {
      ctx.fillText(text, x + offsetX, y)
    }

    rowIndex++
  }

  // æ¢å¤çŠ¶æ€
  ctx.setTransform(1, 0, 0, 1, 0, 0)
  ctx.globalAlpha = 1.0
}

const downloadImage = () => {
  if (!canvasRef.value) return
  const link = document.createElement('a')
  link.download = `watermark_${Date.now()}.png`
  link.href = canvasRef.value.toDataURL('image/png')
  link.click()
}

// ---------------- ç›‘å¬å˜åŒ– ----------------
